package com.potens.schemerecommender.enums;

/**
 * Represents the 10 profile attributes used in eligibility rule evaluation.
 * Each value maps to a field in ProfileRequest.
 */
public enum ProfileAttribute {
    AGE,
    ANNUAL_INCOME,
    GENDER,
    CATEGORY,
    STATE,
    OCCUPATION,
    IS_RURAL,
    HAS_DISABILITY,
    EDUCATION_LEVEL,
    FAMILY_SIZE
}
